package com.web.demo1.bean.district;

public class District {
    private String districtName;
    private double districtSoldprice;
    private int districtSoldnum;
    private double districtRentprice;
    private int districtRentnum;

    public String getDistrictName() {
        return districtName;
    }

    public void setDistrictName(String districtName) {
        this.districtName = districtName;
    }

    public double getDistrictSoldprice() {
        return districtSoldprice;
    }

    public void setDistrictSoldprice(double districtSoldprice) {
        this.districtSoldprice = districtSoldprice;
    }

    public int getDistrictSoldnum() {
        return districtSoldnum;
    }

    public void setDistrictSoldnum(int districtSoldnum) {
        this.districtSoldnum = districtSoldnum;
    }

    public double getDistrictRentprice() {
        return districtRentprice;
    }

    public void setDistrictRentprice(double districtRentprice) {
        this.districtRentprice = districtRentprice;
    }

    public int getDistrictRentnum() {
        return districtRentnum;
    }

    public void setDistrictRentnum(int districtRentnum) {
        this.districtRentnum = districtRentnum;
    }
}
